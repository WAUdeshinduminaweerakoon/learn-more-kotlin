package com.waudeshindumina.kotlin

//Comparison Operators
//(==  , != , <, >, <= ,>=)
fun main() {
    var isTrueOrFalse = 8 == 5
    println(isTrueOrFalse)//false

    var isTrueOrFalse2 = 8!=5
    println(isTrueOrFalse2)//true

    println("isTrueOrFalse is ${8<2}")//false
    println("isTrueOrFalse is ${8>2}")//true

}